#include "libft_up.h"

int		ft_is_whitespace(char c)
{
	return (c == ' ' || c == '\n' || c == '\t' ||
			c == '\r' || c == '\f' || c == '\a' ||
			c == '\b' || c == '\v' ? 1 : 0);
}
